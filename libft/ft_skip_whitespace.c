#include "libft.h"

char	*ft_skip_whitespace(char *str)
{
	while (*str == 32 ||
			*str == '\n' ||
			*str == '\t' ||
			*str == '\f' ||
			*str == '\r' ||
			*str == '\v')
	{
		str++;
	}
	return (str);
}
