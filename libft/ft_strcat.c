#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	char *ptr;

	if (!*src)
		return (dst);
	ptr = dst;
	while (*ptr != '\0')
		ptr++;
	while (*src != '\0')
	{
		*ptr = *src;
		ptr++;
		src++;
	}
	*ptr = '\0';
	return (dst);
}
